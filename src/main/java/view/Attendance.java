    //Load data to jTable method
    private void loadStudents() throws ClassNotFoundException {
        try {
            Connection connection = DBConnection.getInstance().getConnection();
            
            String course = cmbCourse.getSelectedItem().toString();
            String batch = cmbBatch.getSelectedItem().toString();

            String query = "SELECT studentId, name FROM students WHERE courseName = ? AND batchName = ?";
            PreparedStatement pst = connection.prepareStatement(query);
            pst.setString(1, course);
            pst.setString(2, batch);
            ResultSet rs = pst.executeQuery();

            //Clear the previous rows
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            //Add students to the table
            while(rs.next()) {
                String studentId = rs.getString("studentId");
                String studentName = rs.getString("name");
                model.addRow(new Object[]{studentId, studentName, "Absent"}); // Default status is "Absent"
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
