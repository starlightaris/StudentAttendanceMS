    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
        Connection connection = DBConnection.getInstance().getConnection();

        Date date = (Date) dateChooser.getDate();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            String studentId = model.getValueAt(i, 0).toString();
            String status = model.getValueAt(i, 2).toString(); // Get attendance status ("Present" or "Absent")

            // Insert or update attendance for each student
            String query = "INSERT INTO attendance (studentId, date, status)" +
                           "VALUES (?, ?, ?) ON DUPLICATE KEY UPDATE status = ?";
            PreparedStatement pst = connection.prepareStatement(query);
            pst.setString(1, studentId);
            pst.setDate(2, new java.sql.Date(date.getTime()));
            pst.setString(3, status);
            pst.setString(4, status);  // Update the status if the record already exists
            pst.executeUpdate();
        }
        
            JOptionPane.showMessageDialog(this, "Attendance saved successfully!");
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error saving attendance.");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Attendance.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed

