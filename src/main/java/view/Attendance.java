    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
                                         
        try {
            // Get selected course and batch from the dropdown menus
            String selectedCourse = cmbCourse.getSelectedItem().toString();
            String selectedBatch = cmbBatch.getSelectedItem().toString();

            // Check if both course and batch are selected
            if (selectedCourse.isEmpty() || selectedBatch.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please select both Course and Batch.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Connection connection = DBConnection.getInstance().getConnection();

            // Query to fetch student details based on selected course and batch
            String query = "SELECT studentId, name FROM student " +
                           "WHERE courseId = (SELECT courseId FROM course WHERE courseName = ?) " +
                           "AND batchId = (SELECT batchId FROM batch WHERE batchName = ?)";
            PreparedStatement pst = connection.prepareStatement(query);
            pst.setString(1, selectedCourse);
            pst.setString(2, selectedBatch);

            // Execute query and get results
            ResultSet rs = pst.executeQuery();

            // Get the table model
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            // Clear previous table data
            model.setRowCount(0);

            // Fill in the table with the results
            while (rs.next()) {
                int studentId = rs.getInt("studentId");
                String name = rs.getString("name");
                model.addRow(new Object[]{studentId, name});
            }

        } catch (SQLException | ClassNotFoundException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

