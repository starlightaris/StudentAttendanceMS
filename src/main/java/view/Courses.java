    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        
        Coursedto course = new Coursedto(
            txtCourseName.getText()
        );
        try{            
            Connection connection = DBConnection.getInstance().getConnection();
            
            PreparedStatement pst = connection.prepareStatement("INSERT INTO course(courseName) VALUES (?)");
            pst.setString(1,course.getCourseName());
            
            int save = pst.executeUpdate();
            
            if(save!=0){
                JOptionPane.showMessageDialog(this,"Course added successfully.");
                //Refresh jTable after new record
                loadCourseTableData();
                //Clear the textbox upon button click
                txtCourseName.setText("");
            }
            else{
                JOptionPane.showMessageDialog(this,"Failed to add the course.");
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Courses.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a course to edit.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // Get the selected course ID
            int courseId = (int) jTable1.getValueAt(selectedRow, 0);
            String newCourseName = txtCourseName.getText();

            if (newCourseName.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a new course name.",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            //Update the course in the database
            Connection connection = DBConnection.getInstance().getConnection();
            
            String query = "UPDATE course SET courseName = ? WHERE courseId = ?";
            PreparedStatement pst = connection.prepareStatement(query);
            pst.setString(1, newCourseName);
            pst.setInt(2, courseId);

            int updated = pst.executeUpdate();
            if (updated > 0) {
                JOptionPane.showMessageDialog(this, "Course updated successfully.");
                //Refresh the table
                loadCourseTableData(); 
                //Clear the textbox upon button click
                txtCourseName.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update the course.");
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Courses.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }       
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a course to delete.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            // Get the selected course ID
            int courseId = (int) jTable1.getValueAt(selectedRow, 0);

            // Confirm delete action
            int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this course?",
                    "Confirm Delete", JOptionPane.YES_NO_OPTION);
            if (confirm != JOptionPane.YES_OPTION) {
                return;
            }

            // Delete the course from the database
            Connection connection = DBConnection.getInstance().getConnection();
            
            String query = "DELETE FROM course WHERE courseId = ?";
            PreparedStatement pst = connection.prepareStatement(query);
            pst.setInt(1, courseId);

            int deleted = pst.executeUpdate();
            if (deleted > 0) {
                JOptionPane.showMessageDialog(this, "Course deleted successfully.");
                // Refresh the table
                loadCourseTableData();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete the course.");
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Courses.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

