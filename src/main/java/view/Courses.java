    //Loading data to jTable method
    private void loadCourseTableData() {
    try {
        Connection connection = DBConnection.getInstance().getConnection();

        String query = "SELECT * FROM course"; 
        PreparedStatement pst = connection.prepareStatement(query);
        ResultSet rs = pst.executeQuery();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        model.setRowCount(0);

        //Fill in the table with data from the ResultSet
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("courseId"), 
                rs.getString("courseName")
            });
        }

    } catch (SQLException ex) {
        Logger.getLogger(Courses.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Error loading course data: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } catch (ClassNotFoundException ex) {
        Logger.getLogger(Courses.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Class not found: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    
