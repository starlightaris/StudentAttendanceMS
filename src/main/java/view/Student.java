    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        
        // Get form inputs
        String name = txtName.getText().trim();
        String email = txtEmail.getText().trim().toLowerCase(); 
        String phoneNo = txtPhone.getText().trim();

        //Using the ternary conditional operator to determind the gender type
        String selectedGenderString = (String) cmbGender.getSelectedItem();
        GenderType genderType = getGenderTypeFromString(selectedGenderString);
        
        try{ 
            // Validate inputs
            if (txtName.getText().isEmpty() || txtEmail.getText().isEmpty() || txtPhone.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please fill all fields.",
                        "Validation Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (cmbCourse.getSelectedItem() == null || cmbBatch.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Please select a Course and Batch.",
                        "Validation Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            if (!isValidEmail(email)) {
                JOptionPane.showMessageDialog(this, "Invalid email address. Please enter a valid email.",
                        "Validation Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (!isValidPhoneNumber(phoneNo)) {
                JOptionPane.showMessageDialog(this, "Invalid phone number. Please enter a 10-digit phone number.",
                        "Validation Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            //Connection
            Connection connection = DBConnection.getInstance().getConnection();
            
            //Get courseId
            String courseName = cmbCourse.getSelectedItem().toString();
            PreparedStatement pstCourse = connection.prepareStatement("SELECT courseId FROM course WHERE courseName = ?");
            pstCourse.setString(1, courseName);
            ResultSet rsCourse = pstCourse.executeQuery();
            if (!rsCourse.next()) {
                JOptionPane.showMessageDialog(this, "Invalid Course selected.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            int courseId = rsCourse.getInt("courseId");

            //Get batchId
            String batchName = cmbBatch.getSelectedItem().toString();
            PreparedStatement pstBatch = connection.prepareStatement("SELECT batchId FROM batch WHERE batchName = ?");
            pstBatch.setString(1, batchName);
            ResultSet rsBatch = pstBatch.executeQuery();
            if (!rsBatch.next()) {
                JOptionPane.showMessageDialog(this, "Invalid Batch selected.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            int batchId = rsBatch.getInt("batchId");
            
            Studentdto student = new Studentdto(
                name,
                email,
                phoneNo,
                genderType,
                courseId,
                batchId
            );
                   
            //Insert the student into the database
            PreparedStatement pst = connection.prepareStatement("INSERT INTO student"
                    + "(name, email, phoneNo, gender, courseId, batchId) VALUES (?,?,?,?,?,?)");
            pst.setString(1,student.getName());
            pst.setString(2,student.getEmail());            
            pst.setString(3,student.getPhoneNo());
            pst.setString(4,selectedGenderString);
            pst.setInt(5, student.getCourseId());
            pst.setInt(6, student.getBatchId());
            
            int save = pst.executeUpdate();
            
            if(save!=0){
                JOptionPane.showMessageDialog(this,"Record Added");
                loadStudentTableData(); //refresh jTable
                Clear(); //Clears all fields
            }
            else{
                JOptionPane.showMessageDialog(this,"Record Failed");
            }
            
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        }       
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a student to delete.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        //Confirm deletion
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this student?",
            "Delete Confirmation",JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);

        //If the user confirms deletion
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                //Get the studentId of the selected row
                int studentId = (int) jTable1.getValueAt(selectedRow, 0);

                //Delete the record from the database
                Connection connection = DBConnection.getInstance().getConnection();
                PreparedStatement pst = connection.prepareStatement("DELETE FROM student WHERE studentId = ?");
                pst.setInt(1, studentId);

                int deleted = pst.executeUpdate();

                if (deleted > 0) {
                    JOptionPane.showMessageDialog(this, "Student deleted successfully.");
                    //Refresh the table after deletion
                    loadStudentTableData();
                    //Clears all fields
                    Clear(); 
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to delete the student.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

