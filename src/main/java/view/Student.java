    //Gender Type Enum conversion to string
    private static GenderType getGenderTypeFromString(String genderString){
        for(GenderType genderType : GenderType.values()){
            if(genderType.name().equalsIgnoreCase(genderString)){
                return genderType;
            }
        }
        throw new IllegalArgumentException("Invalid gender string: " + genderString);
    }
    
    //Load jTable with student info method
    private void loadStudentTableData() {
        try {
            
            Connection connection = DBConnection.getInstance().getConnection();

            String query =  "SELECT student.studentId, student.name, student.email, student.phoneNo, student.gender,"
                     + "course.courseName, batch.batchName "
                     + "FROM student "
                     + "INNER JOIN course ON student.courseId = course.courseId "
                     + "INNER JOIN batch ON student.batchId = batch.batchId";
            PreparedStatement pst = connection.prepareStatement(query);
            ResultSet rs = pst.executeQuery();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            model.setRowCount(0);

            //Fill in the table with data from the ResultSet
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("studentId"), 
                    rs.getString("name"),
                    rs.getString("email"),
                    rs.getString("phoneNo"),
                    rs.getString("gender"),
                    rs.getString("courseName"),
                    rs.getString("batchName")
                });
            }

        } catch (SQLException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error loading course data: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Class not found: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    //Validation methods for email, password, phone number
    private boolean isValidEmail(String email) {
        // Regex for a valid email address
        String emailRegex = "^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,6}$";
        return email.matches(emailRegex);
    }    
    
    private boolean isValidPhoneNumber(String phoneNumber) {
        // Regex for a 10-digit phone number
        String phoneRegex = "^[0-9]{10}$";
        return phoneNumber.matches(phoneRegex);
    }
    
